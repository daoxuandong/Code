#include <bits/stdc++.h> 
using namespace std; 
void check(){
    int n,k;cin>>n>>k;
    int a[n];
    for(int i=0;i<n;i++) cin>>a[i];
    sort(a,a+n);
    int j;
    for(int i=0;i<n;i++) {
         j=a[k-1];
    }
    cout<<j<<endl;
}
int main(){
    int t;cin>>t;
    while(t--){
        check();
    }
}